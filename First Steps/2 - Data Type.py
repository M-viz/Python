########################### Data Type #########################################

#Just an introduction with example on the most common data types
# 
# type() method returns class type of the argument(object) passed as parameter. 
# type() function is mostly used for debugging purposes.
# Quote from (https://www.geeksforgeeks.org/python-type-function/)

type(1)
# Out[10]: int
type(2.4)
# Out[11]: float
type(True)
#Out[2]: bool

type("A")
#Out[3]: str

# An important feature of the string data type is that 
# it has Methods, we can apply different methods to strings 

"a".capitalize() # We can avoid to use the print function, it works as well. 
#Out[5]: 'A'


